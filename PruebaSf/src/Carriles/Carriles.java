// Constructor Clase Carriles
public class Carriles {
    int idCarril, Carro, Juego, Pista;

    public int getIdCarril() {
        return idCarril;
    }

    public void setIdCarril(int idCarril) {
        this.idCarril = idCarril;
    }

    public int getCarro() {
        return Carro;
    }

    public void setCarro(int Carro) {
        this.Carro = Carro;
    }

    public int getJuego() {
        return Juego;
    }

    public void setJuego(int Juego) {
        this.Juego = Juego;
    }

    public int getPista() {
        return Pista;
    }

    public void setPista(int Pista) {
        this.Pista = Pista;
    }
}
